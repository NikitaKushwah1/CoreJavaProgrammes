package calculator;

public class Operation {

	public int add(int a,int b)
	{
		int c = a+b;
		return c;
	}
	
	public int multiplicaton(int a,int b)
	{
		int c = a*b;
		return c;
	}
	public int subtraction(int a,int b)
	{
		int c = a-b;
		return c;
	}
	public int division(int a,int b)
	{
		int c = a/b;
		return c;
	}
}
